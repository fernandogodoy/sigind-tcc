    private void salvar() {
        ExpedicaoDao dao = new ExpedicaoDaoImpl();
        expedicao.setEntFuncionario(Login.usuario.getFuncionario());
//        expedicao.setHoraEntExp(Date.valueOf(tfHoraEntrada.getText().replace(":", "")));
//        expedicao.setHoraSaiExp(Date.valueOf(tfHoraSaida.getText().replace(":", "")));
        expedicao.setObsEntExp(tfObsEntrada.getText().trim());
        expedicao.setObsSaiExp(tfObsSaida.getText().trim());
        expedicao.setPesoEntExp(new BigDecimal(tfPesoEntrada.getText().replace(".", "").replace(",", "").trim()));
        expedicao.setPesoSaiExp(new BigDecimal(tfPesoSaida.getText().replace(".", "").replace(",", "").trim()));
        dao.save(expedicao);
        JOptionPane.showMessageDialog(null, "Expedição salva com sucesso");
        limparCampos();
        habilitaCampos(false);
        botoes(true, false, false, true, true, true);
    }
