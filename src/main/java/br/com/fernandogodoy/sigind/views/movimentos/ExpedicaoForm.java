    private void tableConsultaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableConsultaMouseClicked
        if (tableConsulta.getSelectedRow() != -1) {
            expedicao = (tmexpedicao.getLista().get(tableConsulta.getSelectedRow()));
            tfCodigo.setText(expedicao.getIdExpedicao().toString());
            tfHoraEntrada.setText(expedicao.getHoraEntExp().toString());
            tfHoraSaida.setText(expedicao.getHoraSaiExp().toString());
            tfPesoEntrada.setText(new DecimalFormat("").format(expedicao.getPesoEntExp()));
            tfPesoSaida.setText(new DecimalFormat("").format(expedicao.getPesoSaiExp()));
            tfObsEntrada.setText(expedicao.getObsEntExp());
            tfObsSaida.setText(expedicao.getObsSaiExp());
            consulta(false);
        }
    }//GEN-LAST:event_tableConsultaMouseClicked

